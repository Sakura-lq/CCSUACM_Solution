void solve()
{
    int n, k;
    cin >> n >> k;

    vector<int> a(n);
    for (int i = 0; i < n; i++)
    {
        cin >> a[i];
    }

    set<int> st(a.begin(), a.end());
    vector<int> b(st.begin(), st.end());

    if (b.size() < k)
    {
        cout << -1 << endl;
        return;
    }

    cout << b[b.size() - k] << ' ' << b[b.size() - k] << endl;
    return;
}

